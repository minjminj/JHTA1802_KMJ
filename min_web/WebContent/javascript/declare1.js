/**
 * 자바스크립트를 외부 메일에 선언한 후
 * script 태그를 사용하여 참조하는 파일 연습
 */

// 자바스크립트에선 모든 객체나 함수는 function으로 선언
function funcRed(){
	document.body.style.backgroundColor='#ff8888';
	document.body.style.color='#aaaaaa';
	
}

function funcGreen(){
	document.body.style.backgroundColor='#88ff88';
	document.body.style.color='#aaaaaa';
	
}

function funcBlue(){
	document.body.style.backgroundColor='#8888ff';
	document.body.style.color='#aaaaaa';
	
}